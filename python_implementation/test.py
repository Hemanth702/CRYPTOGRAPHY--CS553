from binascii import unhexlify,hexlify
from PRIDE import *

key1 = unhexlify(b"44444444444444444444444444444444")
plaintext1 = unhexlify(b"8888888888888888")
obj = PRIDE(key1)
enc1 = obj.encryption(plaintext1)
print("Encryption of "+str(hexlify(plaintext1))+" using key: "+str(hexlify(key1)))
print(hexlify(enc1))
dec1 = obj.decryption(enc1)
print("Decryption of "+str(hexlify(enc1))+" using key: "+str(hexlify(key1)))
print(hexlify(dec1))
print("\n")

